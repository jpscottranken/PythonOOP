# Function
def greet(name):
  print(f"Hello, {name}!")

  # Function with parameters
def greet2(name, age):  # 'name', 'age' is a parameter
    print(f"Hello, {name}! You are {age} years old.")

# Call (invoke) the function
greet("Jeffrey Paul Scott") # Output: Hello, Jeffrey Paul Scott!

# Call (invoke) the function
greet2("Jeff", 67) # Output: Hello, Jeff! You are 67 years old.
